for (let i = 0; i<5; i++) {
    console.log('Hello World'); //SHow 5 times
}

for (let i = 0; i<5; i++) {
    console.log('Hello World', i); //Show value of i too
}

for (let i = 1; i<=5; i++) {
    if (i % 2 !== 0) console.log(i); // 1 3 5  
}

for (let i = 5; i>=1; i--) {
    if (i % 2 !== 0) console.log(i); // 5 3 1  
}

